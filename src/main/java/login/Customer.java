package login;

public class Customer {
	String name;
	long mobile;
	long accountNo;
	double balance;
	String status;
	public Customer(String name, long mobile, long accountNo, double balance, String status) {
		super();
		this.name = name;
		this.mobile = mobile;
		this.accountNo = accountNo;
		this.balance = balance;
		this.status = status;
	}
	
	
	
}
